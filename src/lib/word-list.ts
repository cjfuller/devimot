// Use a seeded shuffle to ensure that everyone gets the same order of words.
function shuffleInPlace<T>(array: T[]) {
  const rng = seedrandom("divimot-test");
  let currPos = array.length;
  while (currPos > 0) {
    const i = Math.floor(rng() * currPos--);
    const old = array[currPos];
    array[currPos] = array[i];
    array[i] = old;
  }
}

